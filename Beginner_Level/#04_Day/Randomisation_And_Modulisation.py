#In this, I will make ann randomisation and Module concept code
#import is used to importing the modules which are inbuilt in the python or which we have made
import random

#This will print the random number between the number given below in the bracket
randomAge = random.randint(0, 10)

print(randomAge)

#This will print the random float number between 0 and 1
randomFloat = random.random()
print(randomFloat)

#This will multiply the float number between integer and the float between 0 and 1, so the result of output will be float number greater than 0 and 1
print(randomAge + randomFloat)
print(randomAge * randomFloat)

